<template>
  <section>
    <marionette-view
      v-if="ready"
      :key="$route.fullPath"
      :options="options"
      :fetch-on-load="true"
      :mview="mview"
      :breadcrumbs="bc"
      :breadcrumb_tags="bc_tags"
    />
  </section>
</template>

<script>
import MarionetteView from 'app/views/marionette/marionette-wrapper.vue'

import LigandView from 'modules/samples/views/ligandview'
import Ligand from 'models/ligand'

import store from 'app/store/store'
// Allow us to map store values to local computed properties
import { mapGetters } from 'vuex'

export default {
    name: 'LigandViewWrapper',
    components: {
        'marionette-view': MarionetteView
    },
    props: {
        'lid': Number,
    },
    data: function() {
        return {
            ready: false,
            mview: null,
            model: null,
            bc : [],
            bc_tags: ['NAME']
        }
    },
    computed: {
        // Only model needed as option to marionette wrapper
        options: function() {
            return {
                model: this.model,
            }
        },
        // Combine with local computed properties, spread operator
        // Allows us to use this.currentProposal mapped to vuex state/getters
        ...mapGetters('proposal', {
            proposalType: 'currentProposalType'
        })
    },
    created: function() {
        // Set the marionette view constructor we need based on the type
        let title = 'Ligand'

        this.bc = [{ title: title+'s', url: '/'+title.toLowerCase()+'s' }]

        this.model = new Ligand({ LIGANDID: this.lid })

        this.mview = LigandView

        this.ready = true
    },
    beforeRouteEnter: function(to, from, next) {
        // Lookup the proposal first to make sure its a valid id
        store.dispatch('proposal/proposalLookup',  {field: 'LIGANDID', value: to.params.lid})
        .then(() => {
            next()
        }, (error) => {
            store.commit('notifications/addNotification', {title: 'Error looking up proposal from ligand id', msg: error.msg, level: 'error'})
            next('/404')
        })
    }
}
</script>
